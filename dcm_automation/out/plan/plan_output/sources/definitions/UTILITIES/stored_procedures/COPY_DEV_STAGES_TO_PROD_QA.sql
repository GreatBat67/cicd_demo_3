DEFINE PROCEDURE CICD_AUTOMATION_DEV.UTILITIES.COPY_DEV_STAGES_TO_PROD_QA()
RETURNS VARCHAR
LANGUAGE SQL
EXECUTE AS OWNER
AS '
BEGIN

    -- =========================
    -- PATIENTS
    -- =========================
    COPY FILES
    INTO @CICD_AUTOMATION_PROD.PATIENTS.PATIENTS_STAGE
    FROM @CICD_AUTOMATION_DEV.PATIENTS.PATIENTS_STAGE;

    COPY FILES
    INTO @CICD_AUTOMATION_QA.PATIENTS.PATIENTS_STAGE
    FROM @CICD_AUTOMATION_DEV.PATIENTS.PATIENTS_STAGE;

    -- =========================
    -- PATIENT_VISITS
    -- =========================
    COPY FILES
    INTO @CICD_AUTOMATION_PROD.PATIENTS.PATIENT_VISITS_STAGE
    FROM @CICD_AUTOMATION_DEV.PATIENTS.PATIENT_VISITS_STAGE;

    COPY FILES
    INTO @CICD_AUTOMATION_QA.PATIENTS.PATIENT_VISITS_STAGE
    FROM @CICD_AUTOMATION_DEV.PATIENTS.PATIENT_VISITS_STAGE;

    -- =========================
    -- HOSPITALS
    -- =========================
    COPY FILES
    INTO @CICD_AUTOMATION_PROD.HOSPITALS.HOSPITALS_STAGE
    FROM @CICD_AUTOMATION_DEV.HOSPITALS.HOSPITALS_STAGE;

    COPY FILES
    INTO @CICD_AUTOMATION_QA.HOSPITALS.HOSPITALS_STAGE
    FROM @CICD_AUTOMATION_DEV.HOSPITALS.HOSPITALS_STAGE;

    RETURN ''SUCCESS'';

END;
';