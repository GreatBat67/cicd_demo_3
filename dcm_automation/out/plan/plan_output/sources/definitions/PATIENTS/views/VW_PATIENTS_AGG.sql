DEFINE VIEW CICD_AUTOMATION_DEV.PATIENTS.VW_PATIENTS_AGG(
	CITY,
	GENDER,
	PATIENT_COUNT,
	AVG_AGE
) as
SELECT
    CITY,
    GENDER,
    COUNT(*) AS PATIENT_COUNT,
    AVG(DATEDIFF('YEAR', DATE_OF_BIRTH, CURRENT_DATE())) AS AVG_AGE
FROM CICD_AUTOMATION_DEV.PATIENTS.PATIENTS
GROUP BY
    CITY,
    GENDER;