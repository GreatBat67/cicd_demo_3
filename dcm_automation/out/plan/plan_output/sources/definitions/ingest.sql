EMPTY_FIELD_AS_NULL = TRUE;


DEFINE TASK CICD_DEMO_DEV.PROJECTS.INGEST_PATIENTS
    WAREHOUSE = 'DISHA_RANI_WH'
    SCHEDULE = 'USING CRON 30 17 * * * Asia/Kolkata'
    STARTED
AS
BEGIN
    COPY INTO CICD_DEMO_DEV.PROJECTS.PATIENTS
    FROM @CICD_DEMO_DEV.PROJECTS.PATIENTS_STAGE
    FILE_FORMAT = 'CICD_DEMO_DEV.PROJECTS.CICD_DEMO_CSV'
    ;
