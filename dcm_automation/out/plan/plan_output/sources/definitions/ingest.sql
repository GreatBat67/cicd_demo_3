DEFINE STAGE CICD_DEMO_DEV.PROJECTS.PATIENTS_STAGE
    DIRECTORY = ( ENABLE = TRUE )
    COMMENT = 'Internal stage for incoming patient files (CSV)'
;

DEFINE STAGE CICD_DEMO_DEV.PROJECTS.PATIENT_VISITS_STAGE
    DIRECTORY = ( ENABLE = TRUE )
    COMMENT = 'Internal stage for incoming patient visit files (CSV)'
;

define file format CICD_DEMO_DEV.PROJECTS.CICD_DEMO_CSV
    TYPE = CSV
